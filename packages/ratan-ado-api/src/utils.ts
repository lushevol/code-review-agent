const fieldsShouldSplit = [
  "Custom.Testingtypeinscope",
  "System.Tags",
  "Custom.ApplicableSecurityControls",
];
export const parseReleaseWorkitemCustomAndSystemFields = (
  workItemFields: Record<string, any>,
) => {
  return Object.keys(workItemFields).reduce(
    (acc, key) => {
      if (key.startsWith("Custom.") || key.startsWith("System.")) {
        const newKey = key.slice(7);
        const formattedKey = UpperCamelToLowerCamel(newKey); //newKey.charAt(0).toLowerCase() + newKey.slice(1);
        if (fieldsShouldSplit.includes(key)) {
          acc[formattedKey] = (workItemFields[key] ?? "")
            .split(";")
            .map((i: string) => i.trim())
            .filter(Boolean);
        } else if (workItemFields[key]?.displayName) {
          acc[formattedKey] = {
            userId: extractIdFromEmail(workItemFields[key]?.uniqueName),
            displayName: workItemFields[key]?.displayName,
            uniqueName: workItemFields[key]?.uniqueName,
          };
        } else {
          acc[formattedKey] = workItemFields[key] ?? "";
        }
      }
      return acc;
    },
    {} as Record<string, any>,
  );
};

/**
 * Converts a string from UpperCamelCase to lowerCamelCase.
 * @example
 *
 * const result = UpperCamelToLowerCamel("UPPERCamelCase");
 * console.log(result); // Output: "upperCamelCase"
 *
 * @param str The string to convert.
 * @returns The converted lowerCamelCase string.
 */
const UpperCamelToLowerCamel = (str: string) => {
  if (!str) {
    return "";
  }
  let i = 0;
  while (
    i < str.length &&
    str[i] === str[i].toUpperCase() &&
    /[A-Z]/.test(str[i])
  ) {
    i++;
  }
  if (i === 0) {
    return str;
  }
  if (i === 1) {
    return str[0].toLowerCase() + str.slice(1);
  }
  // More than one leading uppercase letter: lower all but the last
  return str.slice(0, i - 1).toLowerCase() + str[i - 1] + str.slice(i);
};
