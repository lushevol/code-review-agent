export const plainWorkItemPayload2JsonPatchDocument = (
  payload: Record<string, string>,
  op: Operation,
): JsonPatchOperation[] => {
  const patchDocument: JsonPatchOperation[] = [];

  for (const [key, value] of Object.entries(payload)) {
    patchDocument.push({
      op,
      path: key,
      value,
    });
  }

  return patchDocument;
};

export const objectWorkItemPayload2JsonPatchDocument = (
  path: string,
  payload: { rel: string; url: string; attributes?: { [key: string]: any } }[],
  op: Operation,
): JsonPatchOperation[] => {
  const patchDocument: JsonPatchOperation[] = [];

  for (const { rel, url, attributes } of payload) {
    const value = { rel, url, attributes };
    patchDocument.push({
      op,
      path,
      value,
    });
  }

  return patchDocument;
};

export const parseDeploymentComponents = (text: string): string[] => {
  return (
    String(html2markdown(text))
      .split("\n")
      .map((i) => i.trim())
      .map((i) => i.replace(/\\_/g, "_"))
      .filter(Boolean) || []
  );
};

export const splitDeploymentComponents = (text: string): string[] => {
  return (
    text
      .split("_")
      .map((i) => i.trim())
      .filter(Boolean) || []
  );
};

// parse 51358-mfe-cashflow-blotter-node-ansible-released-https://dev.azure.com/sc-ado/FMQPR/_build/results?buildId=6908962
// to ["51358-mfe-cashflow-blotter", 6908962]
export const parsePipelineRunFromReleaseDoc = (
  run: string,
  deploymentComponents: string[],
): {
  repo: string;
  command: string;
  buildId: number;
  buildUrl: string;
} => {
  const index = run.lastIndexOf("https://dev.azure.com/");
  if (index === -1) {
    return { repo: run, command: "", buildId: 0, buildUrl: "" };
  }
  const buildUrl = run.substring(index);
  const buildIdMatch = buildUrl?.match(/buildId=(\d+)/);
  const buildId = buildIdMatch ? Number.parseInt(buildIdMatch[1], 10) : 0;
  const repoPart = run.substring(0, index - 1);

  const deploymentComponentsStartedList = deploymentComponents.filter((dc) =>
    repoPart.startsWith(dc),
  );

  const deploymentComponentsStarted = deploymentComponentsStartedList
    .sort((a, b) => b.length - a.length)
    .at(0);

  if (deploymentComponentsStarted) {
    const [repo, ...rest] = splitDeploymentComponents(
      deploymentComponentsStarted,
    );

    return { repo, command: rest.join("_"), buildId, buildUrl };
  }

  return { repo: repoPart, command: "", buildId, buildUrl };
};

